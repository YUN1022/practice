package array;

public class Main {

	public static void main(String[] args) {
	    
	    //primitive type array
	    int[] nArray = {1,3,5,7};
	    
	      for(int i = 0; i < nArray.length; i++) {
	      System.out.println(nArray[i]);
	      }
	    
	    
	    //class type array
	    String[] sArray = new String[5];
	    
	    for(int i = 0; i < sArray.length; i++) {
	        System.out.println(sArray[i]);
	      }


	  }


}
